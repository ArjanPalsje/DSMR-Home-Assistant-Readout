void setClock() {
   // Set time via NTP, as required for x.509 validation
  configTime(3 * 3600, 0, "pool.ntp.org", "time.nist.gov");
  Serial.print("Waiting for NTP time sync: ");
  time_t now = time(nullptr);
  unsigned long start = millis();
  while (now < 8 * 3600 * 2) {
    delay(500);
    Serial.print(".");
    now = time(nullptr);
    //If timesync takes too long, reset the ESP
    if(millis() - start > 15000)
    {
      ESP.restart();
    }
  }
  
  Serial.println("");
  struct tm timeinfo;
  gmtime_r(&now, &timeinfo);
  Serial.print("Current time: ");
  Serial.print(asctime(&timeinfo));
  
}

void FirmwareUpdate() {
  WiFiClientSecure client;
  client.setTrustAnchors(&cert);

  Serial.println("New Firmware Detected");
  Serial.println("Trying to install now!");

  ESPhttpUpdate.setLedPin(12, LOW);
  
  t_httpUpdate_return ret = ESPhttpUpdate.update(client, URL_fw_Bin);

  switch (ret) {
    case HTTP_UPDATE_FAILED:
      Serial.printf("HTTP_UPDATE_FAILD Error (%d): %s\n", ESPhttpUpdate.getLastError(), ESPhttpUpdate.getLastErrorString().c_str());
      break;

    case HTTP_UPDATE_NO_UPDATES:
      Serial.println("HTTP_UPDATE_NO_UPDATES");
      break;

    case HTTP_UPDATE_OK:
      Serial.println("HTTP_UPDATE_OK");
      break;
  }
}
double getLatestFWVersion() {
  Serial.println("Checking for Firmware Update");
  WiFiClientSecure client;
  client.setInsecure();

   HTTPClient http;
   http.begin(client, URL_fw_Ver);
 
  if(http.GET() == 200)
  {
    String req = http.getString();
    http.end();
    return req.toDouble();
  }
  else
  {
    http.end();
    return 0;
  }
}

void handleFWUpdate() {
  //First check if there is an update available
  setClock();
  double latestFW = getLatestFWVersion();
Serial.println("Latest Version: " + String(latestFW));
  if (latestFW > CURRENT_FW_VERSION.toDouble()) {
    FirmwareUpdate();
  }
  else {
  
    Serial.println("Latest Firmware is already installed!");
  
  }
}